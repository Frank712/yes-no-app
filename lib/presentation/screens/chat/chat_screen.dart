        child: Column(
          children: [
            Expanded(
                child: ListView.builder(
              itemCount: 100,
              itemBuilder: (context, index) {
                return (index % 2 == 0)
                    ? const MyMessageBuble()
                    : const HerMessageBuble();
              },
            )),
            const MessageFieldBox(),
          ],
        ),
      ),
    );
  }
